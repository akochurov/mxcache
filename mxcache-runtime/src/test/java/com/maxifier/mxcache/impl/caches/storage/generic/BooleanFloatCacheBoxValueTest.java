@Test
public class BooleanFloatCacheBoxValueTest {
    private static final BooleanFloatCalculatable CALCULATABLE = new BooleanFloatCalculatable() {
        @Override
        public float calculate(Object owner, boolean o) {
            assert o == true;
            return 42f;
        }
    };

    public void testMiss() {
        BooleanObjectStorage storage = mock(BooleanObjectStorage.class);

        when(storage.load(true)).thenReturn(Storage.UNDEFINED);
        when(storage.size()).thenReturn(0);

        BooleanFloatCache cache = (BooleanFloatCache) Wrapping.getFactory(new Signature(boolean.class, Object.class), new Signature(boolean.class, float.class), false).
                wrap("123", CALCULATABLE, storage, new MutableStatisticsImpl());
        cache.setDependencyNode(DependencyTracker.DUMMY_NODE);

        assert cache.getSize() == 0;
        assert cache.getStatistics().getHits() == 0;
        assert cache.getStatistics().getMisses() == 0;

        assert cache.getOrCreate(true) == 42f;

        assert cache.getStatistics().getHits() == 0;
        assert cache.getStatistics().getMisses() == 1;

        verify(storage).size();
        verify(storage, atLeast(1)).load(true);
        verify(storage).save(true, 42f);
        verifyNoMoreInteractions(storage);
    }

    public void testHit() {
        BooleanObjectStorage storage = mock(BooleanObjectStorage.class);

        BooleanFloatCache cache = (BooleanFloatCache) Wrapping.getFactory(new Signature(boolean.class, Object.class), new Signature(boolean.class, float.class), false).
                wrap("123", CALCULATABLE, storage, new MutableStatisticsImpl());
        cache.setDependencyNode(DependencyTracker.DUMMY_NODE);

        when(storage.load(true)).thenReturn(42f);
        when(storage.size()).thenReturn(1);

        assert cache.getSize() == 1;
        assert cache.getStatistics().getHits() == 0;
        assert cache.getStatistics().getMisses() == 0;

        assert cache.getOrCreate(true) == 42f;

        assert cache.getStatistics().getHits() == 1;
        assert cache.getStatistics().getMisses() == 0;

        verify(storage).size();
        verify(storage, atLeast(2)).load(true);
        verifyNoMoreInteractions(storage);
    }

    public void testClear() {
        BooleanObjectStorage storage = mock(BooleanObjectStorage.class);

        BooleanFloatCache cache = (BooleanFloatCache) Wrapping.getFactory(new Signature(boolean.class, Object.class), new Signature(boolean.class, float.class), false).
                wrap("123", CALCULATABLE, storage, new MutableStatisticsImpl());
        cache.setDependencyNode(DependencyTracker.DUMMY_NODE);

        cache.clear();

        verify(storage).clear();
        verifyNoMoreInteractions(storage);
    }

    public void testSetDuringDependencyNodeOperations() {
        BooleanObjectStorage storage = mock(BooleanObjectStorage.class);

        when(storage.load(true)).thenReturn(Storage.UNDEFINED, 42f);

        BooleanFloatCalculatable calculatable = mock(BooleanFloatCalculatable.class);
        MxResource r = mock(MxResource.class);
        when(calculatable.calculate("123", true)).thenThrow(new ResourceOccupied(r));

        BooleanFloatCache cache = (BooleanFloatCache) Wrapping.getFactory(new Signature(boolean.class, Object.class), new Signature(boolean.class, float.class), false).
                wrap("123", calculatable, storage, new MutableStatisticsImpl());
        cache.setDependencyNode(DependencyTracker.DUMMY_NODE);

        assert cache.getStatistics().getHits() == 0;
        assert cache.getStatistics().getMisses() == 0;

        assert cache.getOrCreate(true) == 42f;

        assert cache.getStatistics().getHits() == 1;
        assert cache.getStatistics().getMisses() == 0;

        verify(storage, atLeast(2)).load(true);
        verifyNoMoreInteractions(storage);
        verify(calculatable).calculate("123", true);
        verifyNoMoreInteractions(calculatable);
    }

    public void testResetStat() {
        BooleanObjectStorage storage = mock(BooleanObjectStorage.class);

        when(storage.load(true)).thenReturn(Storage.UNDEFINED);

        BooleanFloatCache cache = (BooleanFloatCache) Wrapping.getFactory(new Signature(boolean.class, Object.class), new Signature(boolean.class, float.class), false).
                wrap("123", CALCULATABLE, storage, new MutableStatisticsImpl());
        cache.setDependencyNode(DependencyTracker.DUMMY_NODE);

        assert cache.getStatistics().getHits() == 0;
        assert cache.getStatistics().getMisses() == 0;

        assert cache.getOrCreate(true) == 42f;

        assert cache.getStatistics().getHits() == 0;
        assert cache.getStatistics().getMisses() == 1;

        cache.getStatistics().reset();

        assert cache.getStatistics().getHits() == 0;
        assert cache.getStatistics().getMisses() == 0;

        verify(storage, atLeast(1)).load(true);
        verify(storage).save(true, 42f);
        verifyNoMoreInteractions(storage);
    }
}
